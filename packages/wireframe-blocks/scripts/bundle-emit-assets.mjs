/**
 * Capture the block + primitive sources as strings so the Convex runtime — which
 * can't read repo files — can emit a REAL shadcn project. Run after editing any
 * block, vendored primitive, or the cn helper:
 *
 *   bun run scripts/bundle-emit-assets.mjs
 *
 * Output (committed): src/emit/emit-assets.generated.ts
 * CI re-runs this and diffs to catch drift between source and the bundle.
 *
 * Rewrites import paths so the emitted files use a standard standalone shadcn
 * layout: blocks import `@/components/ui/*`, primitives import `@/lib/utils`.
 */
import { readdirSync, readFileSync, writeFileSync } from 'node:fs';
import { dirname, join } from 'node:path';
import { fileURLToPath } from 'node:url';

const here = dirname(fileURLToPath(import.meta.url));
const root = join(here, '..', 'src');

// Block / shared-helper source, used at src/components/wireframe-blocks/<id>.tsx.
// In the package they import the vendored primitives by relative path; in an
// emitted project those live at @/components/ui/*.
const blocksDir = join(root, 'blocks');
const blockSources = {};
for (const file of readdirSync(blocksDir).filter((f) => f.endsWith('.tsx')).sort()) {
    const id = file.replace(/\.tsx$/, '');
    const src = readFileSync(join(blocksDir, file), 'utf8').replace(
        /(["'])\.\.\/vendor\/ui\//g,
        '$1@/components/ui/',
    );
    blockSources[id] = src;
}

// Vendored standard shadcn primitives, emitted at src/components/ui/<name>.tsx.
// They import cn from a package-relative path; in an emitted project that's
// @/lib/utils.
const uiDir = join(root, 'vendor', 'ui');
const uiSources = {};
for (const file of readdirSync(uiDir).filter((f) => f.endsWith('.tsx')).sort()) {
    const name = file.replace(/\.tsx$/, '');
    const src = readFileSync(join(uiDir, file), 'utf8').replace(
        /(["'])\.\.\/lib\/utils(["'])/g,
        '$1@/lib/utils$2',
    );
    uiSources[name] = src;
}

const cnSource = readFileSync(join(root, 'vendor', 'lib', 'utils.ts'), 'utf8');

// npm deps the emitted project needs for the vendored primitives + icons. Pinned
// to the same ranges @weblab/ui uses. Keep in sync with the vendored primitives.
const EMIT_DEPS = {
    '@radix-ui/react-accordion': '^1.2.8',
    '@radix-ui/react-avatar': '^1.1.7',
    '@radix-ui/react-separator': '^1.1.4',
    '@radix-ui/react-slot': '^1.2.3',
    'class-variance-authority': '^0.7.1',
    clsx: '^2.1.1',
    'lucide-react': '^0.544.0',
    'tailwind-merge': '^2.3.0',
};

const obj = (map) =>
    Object.entries(map)
        .map(([k, v]) => `    ${JSON.stringify(k)}: ${JSON.stringify(v)},`)
        .join('\n');

const out = `/* eslint-disable */
/**
 * AUTO-GENERATED by scripts/bundle-emit-assets.mjs — do not edit by hand.
 * Regenerate with \`bun run generate:emit-assets\`.
 *
 * Emit-ready source for a standalone shadcn Next.js project:
 *  - BLOCK_SOURCES: src/components/wireframe-blocks/<id>.tsx (+ _ui)
 *  - UI_SOURCES:    src/components/ui/<name>.tsx (standard shadcn primitives)
 *  - CN_SOURCE:     src/lib/utils.ts
 *  - EMIT_DEPS:     npm deps to add to the project's package.json
 */
export const BLOCK_SOURCES: Record<string, string> = {
${obj(blockSources)}
};

export const UI_SOURCES: Record<string, string> = {
${obj(uiSources)}
};

export const CN_SOURCE: string = ${JSON.stringify(cnSource)};

export const EMIT_DEPS: Record<string, string> = {
${obj(EMIT_DEPS)}
};
`;

writeFileSync(join(root, 'emit', 'emit-assets.generated.ts'), out, 'utf8');
console.log(
    `Wrote emit-assets.generated.ts: ${Object.keys(blockSources).length} blocks, ${Object.keys(uiSources).length} primitives.`,
);
