/**
 * Capture each self-contained block source (src/blocks/*.tsx) as a string map so
 * the Convex runtime — which can't read repo files — can emit them verbatim into
 * a generated project. Run after editing any block:
 *
 *   bun run scripts/bundle-emit-assets.mjs
 *
 * Output (committed): src/emit/emit-assets.generated.ts
 * CI re-runs this and diffs to catch drift between block source and the bundle.
 */
import { readdirSync, readFileSync, writeFileSync } from 'node:fs';
import { dirname, join } from 'node:path';
import { fileURLToPath } from 'node:url';

const here = dirname(fileURLToPath(import.meta.url));
const blocksDir = join(here, '..', 'src', 'blocks');
const outFile = join(here, '..', 'src', 'emit', 'emit-assets.generated.ts');

const entries = readdirSync(blocksDir)
    .filter((f) => f.endsWith('.tsx'))
    .sort();

const map = {};
for (const file of entries) {
    const id = file.replace(/\.tsx$/, '');
    map[id] = readFileSync(join(blocksDir, file), 'utf8');
}

const body = Object.entries(map)
    .map(([id, src]) => `    ${JSON.stringify(id)}: ${JSON.stringify(src)},`)
    .join('\n');

const out = `/* eslint-disable */
/**
 * AUTO-GENERATED by scripts/bundle-emit-assets.mjs — do not edit by hand.
 * Source: src/blocks/*.tsx. Regenerate with \`bun run generate:emit-assets\`.
 *
 * Maps blockId (and "_ui") -> the exact self-contained block source, copied
 * verbatim into emitted projects under src/components/wireframe-blocks/.
 */
export const BLOCK_SOURCES: Record<string, string> = {
${body}
};
`;

writeFileSync(outFile, out, 'utf8');
console.log(`Wrote ${outFile} with ${entries.length} block sources.`);
